package ru.job4j.inheritance;

public class Profession {
    String name;
    String surname;
    String education;
    String birthday;
    int yearsExperience;

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getEducation() {
        return education;
    }

    public String getBirthday() {
        return birthday;
    }

    public int getYearsExperience() {
        return yearsExperience;
    }
}

